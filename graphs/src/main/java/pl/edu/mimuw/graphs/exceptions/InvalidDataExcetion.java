package pl.edu.mimuw.graphs.exceptions;

public class InvalidDataExcetion extends GraphsException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7670500993005111364L;

	public InvalidDataExcetion() {
		super();
	}

	public InvalidDataExcetion(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvalidDataExcetion(String arg0) {
		super(arg0);
	}

	public InvalidDataExcetion(Throwable arg0) {
		super(arg0);
	}

}
